@app.route('/api/chat', methods=['POST'])
def chat():
    data = request.get_json()
    user_message = data.get('message', '')

    if not user_message:
        return jsonify({'reply': 'Напишите ваш вопрос, и я постараюсь помочь.'})

    reply = 'Извините, я ещё не знаю ответа на этот вопрос. Оставьте свои контакты, и наш менеджер свяжется с вами.'
    for pattern, answer in FAQ.items():
        if re.search(pattern, user_message):
            # Если ответ содержит подстановку \1, заменяем её на найденную модель
            if r'\1' in answer:
                match = re.search(pattern, user_message)
                if match and match.groups():
                    answer = answer.replace(r'\1', match.group(1))
            reply = answer
            break

    return jsonify({'reply': reply})

